class forlooptest{

	public static void main(String[] args) {
		
		for (int i =0;i<10 ;i++ ) {
			for (int x=0;x<=i ;x++ ) {
				System.out.print("* ");
				
			}
			System.out.println("");
		}
		for (int y =0;y<9 ;y++) {
			for (int a=9;a>y ;a-- ) {
				System.out.print("* ");
				
			}
			System.out.println("");
		}


	}


}