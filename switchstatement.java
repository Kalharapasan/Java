class switchstatement{

	public static void main(String[] args) {
		
		int dayno=7;
		switch (dayno){

			case 1:
				System.out.println("Sundya");
				break;
			case 2:
				System.out.println("Mondya");
				break;
			case 3:
				System.out.println("Tusedya");
				break;
			case 4:
				System.out.println("Wednedya");
				break;
			case 5:
				System.out.println("Thursdya");
				break;
			case 6:
				System.out.println("Fridya");
				break;
			case 7:
				System.out.println("Saturdya");
				break;
			default:
				System.out.println("Invalid Number");
				break;
		}
	}
}