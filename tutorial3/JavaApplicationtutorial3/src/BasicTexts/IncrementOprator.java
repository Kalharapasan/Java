        System.out.println("Frist2:-"+num2);
        
        --num2;
        System.out.println("Second2:-"+num2);
        
        System.out.println("");
        
        int num3 = 25;
        
        System.out.println("Frist3:-"+num3);
        
        num3+=num3;
        System.out.println("Second3:-"+num3);
        
        System.out.println("");
        
        
        int num4 = 25;
        
        System.out.println("Frist4:-"+num4);
        
        num4*=num4;
        System.out.println("Second4:-"+num4);
        
        
        System.out.println("");
