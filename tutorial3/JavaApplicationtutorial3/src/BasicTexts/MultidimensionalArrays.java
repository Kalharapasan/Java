package BasicTexts;

public class MultidimensionalArrays {
    
    public static void main(String[] args) {
        
        int num[][]={{1,1,1},{2,2,2}};
        
        System.out.println("Arraya Value is :-"+num[0][0]);
        System.out.println("Arraya Value is :-"+num[0][1]);
        System.out.println("Arraya Value is :-"+num[0][2]);
        System.out.println("Arraya Value is :-"+num[1][0]);
        System.out.println("Arraya Value is :-"+num[1][1]);   
        System.out.println("Arraya Value is :-"+num[1][2]);
        

    }
    
}
